public class MissEntity {
    private Integer img;
    private String name;
    private String time;
    private Integer contentImg;

    public Integer getImg() {
        return img;
    }

    public void setImg(Integer img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public Integer getContentImg() {
        return contentImg;
    }

    public void setContentImg(Integer contentImg) {
        this.contentImg = contentImg;
    }
}
